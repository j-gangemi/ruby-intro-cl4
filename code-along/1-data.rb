# In Ruby, there are different *types* of data:

# Numbers
puts 3
puts 5

puts 3+1 
puts 5*2

puts 3.0/5.0

# Non-numbers
puts "3+5"

# True, False
puts true
puts false

# Nothing

# Variables
x = 3
y = 5
puts x
puts y
puts x + y
z = 3 + 4
puts z
puts e = "Tacos"
puts tacos = "Tacos"

# Combine strings and variables
puts "Tacos on today's menu: #{x}"
puts "#{e} on today's menu: #{x}"
puts "#{tacos} on today's menu: #{x}"

